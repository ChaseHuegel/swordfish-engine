public static class KeyExtensions
{
    public static string ToDisplayString(this Key key)
    {
        if (key >= Key.D0 && key <= Key.D9)
        {
            return (key - Key.D0).ToString();
        }

        return key.ToString().Replace('_', ' ');
    }
}